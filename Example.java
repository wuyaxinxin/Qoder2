public class Example {
    
    // 私有成员变量
    private String name;
    private int value;
    
    /**
     * 默认构造函数
     */
    public Example() {
        this.name = "Default";
        this.value = 0;
    }
    
    /**
     * 带参数的构造函数
     * 
     * @param name 名称
     * @param value 数值
     */
    public Example(String name, int value) {
        this.name = name;
        this.value = value;
    }
    
    /**
     * 获取名称
     * 
     * @return 名称
     */
    public String getName() {
        return name;
    }
    
    /**
     * 设置名称
     * 
     * @param name 名称
     */
    public void setName(String name) {
        this.name = name;
    }
    
    /**
     * 获取数值
     * 
     * @return 数值
     */
    public int getValue() {
        return value;
    }
    
    /**
     * 设置数值
     * 
     * @param value 数值
     */
    public void setValue(int value) {
        this.value = value;
    }
    
    /**
     * 计算并返回值的平方
     * 
     * @return 值的平方
     */
    public int calculateSquare() {
        return value * value;
    }
    
    /**
     * 显示对象信息
     */
    public void displayInfo() {
        System.out.println("Name: " + name);
        System.out.println("Value: " + value);
        System.out.println("Square: " + calculateSquare());
    }
    
    /**
     * 主方法 - 程序入口
     * 
     * @param args 命令行参数
     */
    public static void main(String[] args) {
        // 创建Example对象
        Example example1 = new Example();
        Example example2 = new Example("Test", 10);
        
        System.out.println("=== Example 1 ===");
        example1.displayInfo();
        
        System.out.println("\n=== Example 2 ===");
        example2.displayInfo();
        
        // 修改值
        example2.setValue(5);
        System.out.println("\n=== After Update ===");
        example2.displayInfo();
    }
    
    @Override
    public String toString() {
        return "Example{name='" + name + "', value=" + value + "}";
    }
}
