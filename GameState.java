/**
 * 游戏状态枚举类
 * 定义游戏的各种状态，用于跟踪和管理游戏的当前运行情况
 */
public enum GameState {
    /**
     * 游戏尚未开始
     * 初始状态，玩家还未开始游戏
     */
    NOT_STARTED,
    
    /**
     * 游戏正在进行中
     * 玩家正在游戏中，游戏逻辑正常更新
     */
    RUNNING,
    
    /**
     * 游戏已暂停
     * 游戏被暂时停止，通常由玩家操作触发
     */
    PAUSED,
    
    /**
     * 游戏结束
     * 游戏已完成，可能是因为胜利或失败条件达成
     */
    GAME_OVER
}
